def get_verse_by_ref(ref: str):
    for v in BIBLE_VERSES:
        if v["ref"] == ref:
            return v
    return None

def get_verses_by_topic(topic: str):
    refs = TOPICS.get(topic.lower(), [])
    verses = []
    for ref in refs:
        v = get_verse_by_ref(ref)
        if v:
            verses.append(v)
    return verses

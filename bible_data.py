def get_verse_count():
    """Возвращает количество стихов в базе"""
    return len(POPULAR_VERSES)

def get_random_verse_key():
    """Возвращает случайную ссылку на стих"""
    import random
    return random.choice(list(POPULAR_VERSES.keys()))

def get_verse_text(key):
    """Возвращает текст стиха по ссылке"""
    return POPULAR_VERSES.get(key, None)

def get_random_verse():
    """‚úÖ –ò–°–ü–†–ê–í–õ–ï–ù–ù–ê–Ø –≤–µ—Ä—Å–∏—è - —Ä–∞–±–æ—Ç–∞–µ—Ç —Å —Ä–µ–∞–ª—å–Ω–æ–π —Å—Ç—Ä—É–∫—Ç—É—Ä–æ–π JustBible API"""
    try:
        book_id, book_name, max_chapter = random.choice(POPULAR_BOOKS)
        chapter = random.randint(1, max_chapter)
        
        logger.info(f"üìñ –ó–∞–ø—Ä–æ—Å —Å—Ç–∏—Ö–∞: {book_name} {chapter}")
        
        url = f"https://justbible.ru/api/book/{book_id}/chapters/{chapter}"
        response = requests.get(url, timeout=15)
        
        logger.info(f"HTTP {response.status_code}")
        
        if response.status_code != 200:
            logger.error(f"API error {response.status_code}: {response.text[:200]}")
            return None
        
        data = response.json()
        logger.info(f"API keys: {list(data.keys())}")
        
        # –ì–∏–±–∫–∏–π –ø–∞—Ä—Å–∏–Ω–≥ —Ä–∞–∑–Ω—ã—Ö —Å—Ç—Ä—É–∫—Ç—É—Ä –æ—Ç–≤–µ—Ç–∞
        verses = (
            data.get('verses') or 
            data.get('chapter', {}).get('verses') or 
            data.get('data', {}).get('verses') or
            []
        )
        
        if not verses or not isinstance(verses, list) or len(verses) == 0:
            logger.error(f"–ù–µ—Ç verses –≤ –æ—Ç–≤–µ—Ç–µ: {list(data.keys())}")
            return None
        
        verse_data = random.choice(verses)
        verse_num = str(verse_data.get('number') or verse_data.get('verse', ''))
        verse_text = verse_data.get('text', '').strip()
        
        if not verse_num or not verse_text:
            logger.error(f"–ù–µ–ø–æ–ª–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ: {verse_data}")
            return None
        
        result = f"{book_name} {chapter}:{verse_num}\n\n{verse_text}"
        logger.info(f"‚úÖ –°—Ç–∏—Ö –ø–æ–ª—É—á–µ–Ω: {result[:100]}...")
        return result
    
    except requests.exceptions.Timeout:
        logger.error("Timeout –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ –∫ JustBible API")
        return None
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ get_random_verse: {type(e).__name__}: {str(e)}")
        logger.error(traceback.format_exc())
        return None

def send_smart_split(chat_id, text):
    """–£–º–Ω–∞—è —Ä–∞–∑–±–∏–≤–∫–∞ —Å –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ–º escape-–ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–µ–π"""
    max_length = 4000
    
    if len(text) <= max_length:
        bot.send_message(chat_id, text, parse_mode='HTML')
        return
    
    # ‚úÖ –ò–°–ü–†–ê–í–õ–ï–ù–û: –ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ –ø–æ \n
    parts = []
    current_part = ""
    lines = text.split('\n')  # –ë—ã–ª–æ '\\n' - –æ—à–∏–±–∫–∞!
    
    for line in lines:
        test_part = current_part + line + '\n'
        if len(test_part) > max_length:
            if current_part.strip():
                parts.append(current_part.strip())
            current_part = line + '\n'
        else:
            current_part = test_part
    
    if current_part.strip():
        parts.append(current_part.strip())
    
    for i, part in enumerate(parts, 1):
        bot.send_message(chat_id, part, parse_mode='HTML')
        logger.info(f"–û—Ç–ø—Ä–∞–≤–ª–µ–Ω–∞ —á–∞—Å—Ç—å {i}/{len(parts)} ({len(part)} —Å–∏–º–≤–æ–ª–æ–≤)")
        time.sleep(0.3)


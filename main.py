def is_bible_reference(text):
    """‚úÖ –£–õ–£–ß–®–ï–ù–ù–´–ô —Ñ–∏–ª—å—Ç—Ä —Å regex"""
    text_lower = text.lower()
    
    # –ü–∞—Ç—Ç–µ—Ä–Ω: —Å–ª–æ–≤–æ_–±–∏–±–ª–∏–∏ + —Ü–∏—Ñ—Ä—ã:—Ü–∏—Ñ—Ä—ã (–≥—Ä–∞–Ω–∏—Ü—ã —Å–ª–æ–≤)
    bible_pattern = r'\b(' + '|'.join(BIBLE_BOOKS) + r')\b.*?\s*(\d+)[.:](\d+)'
    has_reference = bool(re.search(bible_pattern, text_lower))
    
    # –ò–ª–∏ –¥–ª–∏–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç (–≤–µ—Ä–æ—è—Ç–Ω–æ —Å—Ç–∏—Ö)
    is_long = len(text) >= 50
    
    logger.info(f"–§–∏–ª—å—Ç—Ä: reference={has_reference}, long={is_long}, len={len(text)}")
    return has_reference or is_long

